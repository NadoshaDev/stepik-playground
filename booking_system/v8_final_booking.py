all_times = ["10:00", "11:00", "12:00", "13:00"]

# Используем set вместо списка для занятых слотов:
# проверка "in" выполняется быстрее, а дубликаты исключаются автоматически.
busy_times = {"11:00"}

while True:
    # Обновляем список свободных слотов перед каждым циклом выбора.
    # list comprehension делает фильтрацию компактной и читаемой.
    # Условие "not in busy_times" теперь работает эффективно благодаря set.
    free_times = [time for time in all_times if time not in busy_times]

    # Обработка крайнего случая: если все слоты заняты, прерываем программу.
    # Это защищает от ошибок индекса и сразу информирует пользователя.
    if not free_times:
        print("Свободных мест нет. Запись остановлена.")
        break

    # Выводим доступные варианты с нумерацией для удобного выбора.
    for index in range(len(free_times)):
        print(f"{index + 1} - {free_times[index]}")

    # Запрашиваем у пользователя номер выбранного слота.
    # .strip() убирает случайные пробелы, которые часто ломают ввод.
    selected_number = input("Выберите удобное время (введите номер): ").strip()

    # Объединяем все проверки в один цикл while True.
    # Это исключает падение программы (ValueError) и делает логику устойчивой.
    while True:
        # Сначала проверяем тип данных: ввод должен состоять только из цифр.
        if not selected_number.isdigit():
            print("Ошибка: введите только цифру.")
            selected_number = input("Выберите удобное время (введите номер): ").strip()
            continue  # Возвращаемся к началу цикла для повторного ввода

        # Затем проверяем диапазон: номер должен существовать в текущем списке.
        if not 0 < int(selected_number) <= len(free_times):
            print("Ошибка: такого номера нет в списке.")
            selected_number = input("Выберите удобное время (введите номер): ").strip()
            continue  # Снова просим ввести корректное значение

        # Если обе проверки пройдены успешно, выходим из цикла валидации.
        break

    # Преобразуем строку в число и корректируем индекс.
    # Нумерация для пользователя начинается с 1, а индекс списка с 0.
    index = int(selected_number) - 1
    selected_time = free_times[index]

    # Запрашиваем финальное подтверждение, чтобы избежать случайных записей.
    confirmation = input(f"Подтвердить запись на {selected_time}? (да/нет): ").strip().lower()

    if confirmation == "да":
        # Добавляем время в занятые слоты через метод .add() для set.
        # Это гарантирует отсутствие дубликатов и сохраняет порядок проверки.
        busy_times.add(selected_time)
        print(f"Вы записаны на: {selected_time}")
        break  # Завершаем главный цикл программы
    else:
        # Если пользователь отказался, цикл while True начнётся заново.
        print("Выбор отменён, попробуйте снова.")
