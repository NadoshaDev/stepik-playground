all_times = ["10:00", "11:00", "12:00", "13:00"]

import os

# Значение по умолчанию.
# Если программа запускается впервые или файл пока пуст,
# система начинает работу с пустым множеством занятых слотов.
busy_times = set()

# Перед запуском основной логики необходимо восстановить состояние системы.
# Проверяем, существует ли файл с ранее сохранёнными данными.
if os.path.exists("busy_times.txt"):

    # Открываем файл в режиме чтения и загружаем всё содержимое в память.
    file = open("busy_times.txt", "r")
    text = file.read()

    # После чтения данные уже находятся в переменной text,
    # поэтому файл можно сразу закрыть.
    file.close()

    # Если файл содержит сохранённые записи,
    # восстанавливаем множество busy_times.
    if text != "":

        # Каждая запись хранится на отдельной строке.
        # Преобразуем текст в список временных слотов.
        times_list = text.split("\n")

        # Основная логика проекта работает с множеством.
        # Восстанавливаем структуру данных, используемую Booking System.
        busy_times = set(times_list)

else:
    # Первый запуск программы.
    # Если файл ещё не существует, создаём его автоматически,
    # чтобы подготовить систему к дальнейшему сохранению данных.
    file = open("busy_times.txt", "w")

# Закрываем файл после создания.
file.close()

# Главный цикл сценария: управляет повтором записи до успешного завершения или полного отказа
while True:

    # Формируем свободные слоты через list comprehension.
    # Условие "not in busy_times" работает эффективно благодаря set.
    free_times = [time for time in all_times if time not in busy_times]

    # Защита от крайнего случая: если свободных слотов не осталось,
    # завершаем работу программы.
    if not free_times:
        print("Свободных мест нет. Запись остановлена.")
        break

    # Вывод нумерованного списка для удобного выбора.
    for index in range(len(free_times)):
        print(f"{index + 1} - {free_times[index]}")

    # Запрос номера свободного слота.
    # .strip() убирает случайные пробелы, которые часто ломают ввод.
    selected_number = input("Выберите удобное время (введите номер): ").strip()

    # Цикл валидации ввода: объединяет проверку типа и диапазона.
    # Исключает ValueError и делает логику устойчивой к любым данным.
    while True:

        # Шаг 1: Проверяем, что введены только цифры.
        if not selected_number.isdigit():
            print("Ошибка: введите только цифру.")
            selected_number = input("Выберите удобное время (введите номер): ").strip()
            continue

        # Шаг 2: Проверяем, что номер попадает в диапазон доступных слотов.
        if not 0 < int(selected_number) <= len(free_times):
            print("Ошибка: такого номера нет в списке.")
            selected_number = input("Выберите удобное время (введите номер): ").strip()
            continue

        # Обе проверки успешно пройдены.
        break

    # Преобразуем номер пользователя в индекс списка.
    # Пользователь считает с единицы, Python — с нуля.
    index = int(selected_number) - 1
    selected_time = free_times[index]

    # Наборы допустимых ответов для подтверждения записи.
    # Используем set для быстрой проверки принадлежности O(1).
    positive_answers = {"да", "yes"}
    negative_answers = {"нет", "no"}

    # Цикл подтверждения: отделяет выбор времени
    # от подтверждения записи и обработки ошибок ввода.
    while True:

        # Нормализуем ввод пользователя.
        # Убираем лишние пробелы и приводим строку к нижнему регистру,
        # чтобы корректно обработать "ДА", " Yes ", "да" и другие варианты.
        confirmation = input(
            f"Подтвердить запись на {selected_time}? (да/нет): "
        ).strip().lower()

        if confirmation in positive_answers:
            busy_times.add(selected_time)
            print(f"Вы записаны на: {selected_time}")
            break  # Успешное подтверждение.

        elif confirmation in negative_answers:
            print("Выбор отменён, попробуйте снова.")
            break  # Возвращаемся к выбору времени.

        else:
            print("Ошибка ввода! Пожалуйста, введите 'да' или 'нет'.")
            continue  # Повторяем запрос подтверждения.

    # Используем сохранённое состояние confirmation после выхода
    # из внутреннего цикла для управления дальнейшим сценарием программы.
    if confirmation in positive_answers:
        print("Хорошего Вам дня! До встречи!")
        exit()  # Корректное завершение программы после успешной записи.
